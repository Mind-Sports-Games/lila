export function abalonePiotrToKey(k: string): Key {
	let i = piotrKeys.findIndex(k);
	if (i < 0) return undefined;
	
	const pos: [number, number] = i < 81? [i%9, i/9]: abaloneHashPosToIndex(i);
	return String.fromCharCode(pos[1] + 97) + "" + (pos[0] + 1) as Key;
}

function abaloneHashPosToIndex(i: number): [number, number] {
	// k² <= i < (k + 1)² => (0, k)...(k, k)...(k, 0)
	const k = Math.floor(Math.sqrt(i));
	const j = i - k*k;
	
	return j < k? [j, k]: [k, 2*k - j];
}